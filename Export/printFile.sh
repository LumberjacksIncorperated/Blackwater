#------------------------------------------------------------------------------------------
# MAIN
#------------------------------------------------------------------------------------------
main()
{
    printPotentialClientList $1
}

#------------------------------------------------------------------------------------------
# INTERNAL FUNCTIONS
#------------------------------------------------------------------------------------------
function printPotentialClientList()
{
    while IFS='' read -r line || [[ -n "$line" ]]; do
        echo ${line:36:17}
    done < "$1"
}
#------------------------------------------------------------------------------------------
# SCRIPT
#------------------------------------------------------------------------------------------
    main $1
